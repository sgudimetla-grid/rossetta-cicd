export function getCarById(id) {
  return cars.find((car) => car.id === id);
}

export function getCarsByCategory(category) {
  if (!category || category === "All") return cars;
  return cars.filter((car) => car.category === category);
}

export function getFeaturedCars() {
  return cars.filter((car) => car.featured);
}

export function getAllBrands() {
  return [...new Set(cars.map((car) => car.brand))].sort();
}

export function getAllCategories() {
  return [...new Set(cars.map((car) => car.category))].sort();
}

export function formatPrice(price) {
  return new Intl.NumberFormat("en-US", {
    style: "currency",
    currency: "USD",
    maximumFractionDigits: 0,
  }).format(price);
}
